import java.util.*;
public class conditional_practice {
    public static void main(String []args){
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        
        if(n>0){
            System.out.println("Given num is Positive");
        }else{
            System.out.println("Given num is Negative");
        }
    }
    
}
